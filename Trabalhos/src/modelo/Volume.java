package modelo;

public class Volume {
	public static double converter1(double i) {
		//metro cúbico
		return i;
	}
	public static double converter2(double i) {
		//centímetros cúbicos
		return i * 1000000;
	}
	public static double converter3(double i) {
		//litros
		return i;
	}
	public static double converter4(double i) {
		//mililitros
		return i * 1000 ;
	}
}
